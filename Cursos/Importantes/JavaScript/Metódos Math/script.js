// 1 - Math.abs
// Retorna o valor absoluto de um número.
console.log(Math.abs(-5.5)) // 5.5


// 2 - Math.ceil
// Arredonda para cima
console.log(Math.ceil(4.8)) // 5


// 3 - Math.floor
// Arredonda para baixo
console.log(Math.floor(4.8)) // 4


// 4 - Math.round
// Arredonda para o número inteiro mais próximo
console.log(Math.round(4.8)) // 5
console.log(Math.round(4.4)) // 4


// 5 - Math.sqrt
// Retorna a raiz quadrada de um número
console.log(Math.sqrt(81)) // 9


// 6 - Math.cbrt
// Retorna a raiz cúbica de um número
console.log(Math.cbrt(27)) // 3


// 7 - Math.pow
// Retorna a base elevada ao expoente
console.log(Math.pow(2, 3)) // 8


// 8 - Math.max
// Retorna o maior número de uma lista de argumentos
console.log(Math.max(1, 2, 23, 4, 5)) // 23


// 9 - Math.min
// Retorna o menor número de uma lista de argumentos
console.log(Math.min(1, 2, 23, -4, 5)) // -4


// 10 - Math.random
// Retorna um número aleatório entre 0 e 1
console.log(Math.random()) // 0.123456789
// Pode se usar para sortear qualquer número, basta usar o sinal de multiplicação (*)
console.log(Math.round(Math.random() * 10)) // 1.23456789


// 11 - Math.PI
// Retorna o valor de PI
console.log(Math.PI) // 3.141592653589793


// 12 - Math.trunc
// Retorna a parte inteira de um número
console.log(Math.trunc(4.8)) // 4
console.log(Math.trunc(4.3)) // 4
